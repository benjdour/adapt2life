const rawExerciseData: Record<GarminExerciseSport, GarminExerciseEntry[]> = {
  STRENGTH_TRAINING: strengthExercises,
  CARDIO_TRAINING: cardioExercises,
  YOGA: yogaExercises,
  PILATES: pilatesExercises,
};

const buildExerciseLookup = (data: typeof rawExerciseData) => {
  const lookup = {} as Record<GarminExerciseSport, Record<string, Set<string>>>;

  (Object.entries(data) as [GarminExerciseSport, GarminExerciseEntry[]][]).forEach(([sport, entries]) => {
    const sportCatalog: Record<string, Set<string>> = {};

    entries.forEach(({ category, name }) => {
      const normalizedCategory = normalizeKey(category);
      const normalizedName = normalizeKey(name);

      if (!normalizedCategory || !normalizedName) {
        return;
      }

      if (!sportCatalog[normalizedCategory]) {
        sportCatalog[normalizedCategory] = new Set<string>();
      }
      sportCatalog[normalizedCategory].add(normalizedName);
    });

    lookup[sport] = sportCatalog;
  });

  return lookup;
};

const exerciseLookup = buildExerciseLookup(rawExerciseData);

export const GARMIN_EXERCISE_DATA = rawExerciseData;
export const GARMIN_EXERCISE_LOOKUP = exerciseLookup;
export const GARMIN_EXERCISE_SPORTS = new Set<GarminExerciseSport>(
  Object.keys(rawExerciseData) as GarminExerciseSport[],
);
export const GARMIN_EXERCISE_CATEGORIES = Object.fromEntries(
  (Object.entries(exerciseLookup) as [GarminExerciseSport, Record<string, Set<string>>][]).map(([sport, catalog]) => [
    sport,
    new Set(Object.keys(catalog)),
  ]),
) as Record<GarminExerciseSport, Set<string>>;

const resolveCatalog = (sport: string | null | undefined) => {
  if (!sport) {
    return null;
  }
  const normalizedSport = sport as GarminExerciseSport;
  if (!GARMIN_EXERCISE_SPORTS.has(normalizedSport)) {
    return null;
  }
  return exerciseLookup[normalizedSport];
};

export const hasGarminExerciseCategory = (sport: string | null | undefined, category: string | null | undefined) => {
  if (!category) {
    return false;
  }
  const catalog = resolveCatalog(sport);
  if (!catalog) {
    return false;
  }
  return catalog[normalizeKey(category)] !== undefined;
};

export const isKnownGarminExercise = (
  sport: string | null | undefined,
  category: string | null | undefined,
  name: string | null | undefined,
) => {
  if (!category || !name) {
    return false;
  }
  const catalog = resolveCatalog(sport);
  if (!catalog) {
    return false;
  }
  const exercises = catalog[normalizeKey(category)];
  if (!exercises) {
    return false;
  }
  return exercises.has(normalizeKey(name));
};

export const getGarminExerciseNames = (sport: string, category: string): string[] => {
  const catalog = resolveCatalog(sport);
  if (!catalog) {
    return [];
  }
  const exercises = catalog[normalizeKey(category)];
  if (!exercises) {
    return [];
  }
  return Array.from(exercises.values());
};

